//Add and remove items using push, pop, shift, unshift.


let Names=["Manikandan","Soundarya"]
console.log(Names);

Names.push("Susi") //push add last
console.log(Names);

Names.pop() //pop remove last
console.log(Names);

Names.unshift("Ranjitha") //unshift add first
console.log(Names);

Names.shift()  //shift remove first
console.log(Names);

